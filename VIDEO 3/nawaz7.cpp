#include<iostream>
using namespace std;

int main()
{
    // CONDITIONAL STATEMENT : if else statement
    int n ;
    cout << "Enter the number : ";
    cin >> n;

    if (n>=0)
    {
        cout << "The number is positive. " << endl;
    }
    else 
    {
        cout << "The number is negative. " << endl;
    }

    return 0;
}